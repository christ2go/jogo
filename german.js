// german.js

var german = new language();

// Funktionen, die Datenstrukturen "erschaffen"

german.addcommand("word",["wort"]);
german.addcommand("list",["liste"]);
german.addcommand("sentence",["satz"]);
german.addcommand("fput",["me","mitletztem"]);
german.addcommand("lput",["ml","miterstem"]);

// Selektoren

german.addcommand("first",["erstes"]);
german.addcommand("firsts",["alleerstes"]);
german.addcommand("last",["letztes","lz"]);
german.addcommand("butfirst",["ohneerstes","oe"]);
